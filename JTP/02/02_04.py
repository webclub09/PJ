### 튜플의 인덱싱과 슬라이싱, 더하기(+)와 곱하기(*)
## 1. 인덱싱하기
t1 = (1, 2, 'a', 'b')
print(t1[0])
#>> 1
print(t1[3])
#>> b

## 2. 슬라이싱하기
t1 = (1, 2, 'a', 'b')
print(t1[1:])
#>> (2, 'a', 'b')

## 3.튜플 더하기
t2 = (3, 4)
print(t1 + t2)
#>> (1, 2, 'a', 'b', 3, 4)

## 4. 튜플 곱하기
print(t2 * 3)
#>> (3, 4, 3, 4, 3, 4)

